package sort;

public class Data {

    public static Integer[] array = { 5, 7, 3, 4, 2, 1, 9, 10, 8, 6 };

    public static void print() {
        for (int count = 0; count < array.length; count++) {
            System.out.print(array[count] + " ");
        }
        System.out.println();
    }

}
