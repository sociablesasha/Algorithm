package sort;

public class Data {

    public static Integer[] array = { 10, 2, 5, 4, 6, 3, 7, 1, 8, 9 };

    public static void print() {
        for (int count = 0; count < array.length; count++) {
            System.out.print(array[count] + " ");
        }
        System.out.println();
    }

}
